"""
Turtle is a Python feature like a drawing board, which lets us command
a turtle to draw all over it! We can use functions like turtle.forward()
and turtle.right() which can move the turtle around.
"""
import turtle

screen = turtle.Screen()
box = turtle.Turtle()
box.forward(50)
box.left(90)
box.forward(50)
box.left(90)
box.forward(50)
box.left(90)
box.forward(50)
box.left(90)

turtle.mainloop()
