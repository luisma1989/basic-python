# Operaciones matematicas
a = int(input('ingrese un número A: '))
b = int(input('ingrese un número B: '))

print('suma:', a + b)
print('resta:', a - b)
print('multiplicacion:', a * b)
print('potencia:', a ** b)
print('division:', a / b)
print('division entero:', a // b)
print('resto:', a % b)
print('A mayor B:', a > b)
print('A menor B:', a < b)
print('A igual B:', a == b)
